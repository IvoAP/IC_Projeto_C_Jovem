# Tuplas são uma estrutura de dados não imútavel , uma vez criada eu não possso modificar minha tupla
#        0 1 2 3 4
tupla = (1,2,3,4,5,2, 2)

#ver a tupla
print(tupla)

#acessar elemento de uma posição específica:
print(tupla[1])

# contar enúmero de vezes de um elemnte d euma tupla
print(tupla.count(2))

#achar o indice de um elemento
print(tupla.index(1))