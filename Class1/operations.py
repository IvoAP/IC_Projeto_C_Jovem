# Operações Básicas
# 1 - Adição
# 2 - Subtrção
# 3 - Multiplicação
# 4 - Divisão
# 5 - Divisão Inteira
# 6 - Potenciação

n1  = float(input("Insira um número: "))
n2 = int(input("Insira um número: "))

# Adição
soma = n1+n2
print(f"Soma:  {soma}")

# Subtração 
sub = n1-n2
print("Subtração: ", sub)

# Multiplicação
print("Multiplicação: ", n1*n2 )

# Potenciação 
print("Potencia: ", n1**n2)

# Divisão
print("Divisão: " , n1/n2)

# Divisão Inteira
print(" Divisão Inteira: ", int(n1//n2))






