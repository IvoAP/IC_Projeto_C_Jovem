# Operações Básicas
# 1 - Adição
# 2 - Subtrção
# 3 - Multiplicação
# 4 - Divisão
# 5 - Divisão Inteira
# 6 - Potenciação

# inteiro (int) - >1, 2 , 3
# float(float) -> 1.0 , 1.25

n1  = int(input("Insira um número: "))
n2 = int(input("Insira um número: "))


print(f"Numero 1: {n1}")
print(f"Numero 2: {n2}")

# Adição
soma = n1+n2
print(f"Soma:  {soma}")

# Subtração 
sub = n1-n2
print("Subtração: ", sub)

# Multiplicação
print("Multiplicação: ", n1*n2 )

# Potenciação 
print("Potencia: ", n1**n2)

# Divisão
print("Divisão: " , n1/n2)

# Divisão Inteira
print(" Divisão Inteira: ", (n1//n2))






